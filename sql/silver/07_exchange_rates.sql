CREATE OR REPLACE TABLE SILVER.EXCHANGE_RATES AS
SELECT 'USD' AS CURRENCY, 1.0000 AS USD_CONVERSION_RATE
UNION ALL SELECT 'EUR', 1.0850
UNION ALL SELECT 'GBP', 1.2700
UNION ALL SELECT 'CAD', 0.7400
UNION ALL SELECT 'AUD', 0.6600
UNION ALL SELECT 'JPY', 0.0068
UNION ALL SELECT 'CNY', 0.1390
UNION ALL SELECT 'INR', 0.0120;
